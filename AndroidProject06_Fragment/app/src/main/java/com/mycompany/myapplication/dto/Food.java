package com.mycompany.myapplication.dto;

public class Food {
    private int fno;
    private String fname;
    private int fphoto;
    private int fstar;
    private String fdesc;

    public int getFno() {
        return fno;
    }

    public void setFno(int fno) {
        this.fno = fno;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public int getFphoto() {
        return fphoto;
    }

    public void setFphoto(int fphoto) {
        this.fphoto = fphoto;
    }

    public int getFstar() {
        return fstar;
    }

    public void setFstar(int fstar) {
        this.fstar = fstar;
    }

    public String getFdesc() {
        return fdesc;
    }

    public void setFdesc(String fdesc) {
        this.fdesc = fdesc;
    }
}
